package com.byeautumn.wb.output;

public class OHLCImageConfig
{
	private int width;
	private int height;
	
	public int getWidth()
	{
		return width;
	}
	public OHLCImageConfig setWidth(int width)
	{
		this.width = width;
		
		return this;
	}
	public int getHeight()
	{
		return height;
	}
	public OHLCImageConfig setHeight(int height)
	{
		this.height = height;
		
		return this;
	}
	


}
